elements.eiffel_tower = {
    color: "#bfbfbf",

    tick: function(pixel) {

        
        if (!pixel.built && !isEmpty(pixel.x, pixel.y + 1)) {

            
            let baseX = pixel.x;
            let baseY = pixel.y;

            
            deletePixel(baseX, baseY);

            
            buildExactEiffel({x: baseX, y: baseY});

            return;
        }

        doDefaults(pixel);
    },

    properties: {
        built: false
    },

    category: "citybuilding",
    state: "solid",
    density: 1500,
    cooldown: defaultCooldown,
    seed: true,
    maxSize: 1,
    excludeRandom: true,

    behavior: behaviors.POWDER,
};
